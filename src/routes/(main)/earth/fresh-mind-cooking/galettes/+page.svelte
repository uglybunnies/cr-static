    <h3 class="subtitle">The Pastry</h3>

    <ul class="clean-list">
      <li>2 1/2 (250 g) cups unbleached organic flour</li>

      <li>1/2 teaspoon salt</li>

      <li>1 1/2 (170 g) sticks unsalted butter, cut into 1/4-inch cubes</li>

      <li>Small glass ice water (4-5 tablespoons)</li>
    </ul>
<br>
    <h3 class="subtitle">For Food Processor</h3>
    <ul class="clean-list">
      <li>Add flour and salt into processor bowl. Pulse couple of times.</li>

      <li>Add butter, pulse 5-10 times until mixture forms pea size crumbs.</li>

      <li>Add 4 tablespoons water and run processor to gather into a ball.</li>

      <li>
        Transfer to a bowl and work into a ball, add tablespoon more water as
        needed until holds together.
      </li>

      <li>Divide dough into two parts, flour lightly</li>
    </ul>
