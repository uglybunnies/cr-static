<br>
    <h3>Asian Dressing</h3>
    <ul class="clean-list">
      <li>2 tablespoons lime juice or rice vinegar</li>

      <li>2 tablespoon soy sauce</li>

      <li>1 tablespoon sesame oil or neutral oil</li>

      <li>1 tablespoon honey, maple syrup or sugar</li>

      <li>1 tablespoon grated fresh ginger</li>

      <li>Sriracha sauce or chili (optional)</li>
    </ul>
<br>
    <h3>Lemon Dressing</h3>
    <ul class="clean-list">
      <li>3 tablespoons olive oil</li>

      <li>1 tablespoon lemon juice</li>

      <li>1 tablespoon grated lemon rind</li>

      <li>1 large clove garlic, grated</li>

      <li>Salt and freshly ground black pepper or chili</li>

      <li>Additions: yogurt and or mustard</li>
    </ul>
