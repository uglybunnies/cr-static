    <h3>Black Bean Soup with Sweet Potatoes</h3>

    <ul class="clean-list">
      <li>1 cup dried black beans, soaked overnight</li>

      <li>1 medium onion, peeled and chopped</li>

      <li>6 cups water</li>

      <li>2 medium carrots, peeled and cut into 1/2-inch pieces</li>

      <li>2-3 sweet potatoes, peeled and cut into 1/2-inch pieces</li>

      <li>Salt to taste</li>
    </ul>
<br>
    <h3 class="subtitle">Sauce</h3>
    <ul class="clean-list">
      <li>3 tablespoons vegetable oil</li>

      <li>1 can (15 ounces) diced tomatoes</li>

      <li>2 large cloves garlic, peeled and finely chopped</li>

      <li>3 tablespoons chopped fresh oregano or 2 teaspoons dried</li>

      <li>1 teaspoon ground cumin</li>

      <li>1 teaspoon salt</li>

      <li>1/4 teaspoon cayenne pepper</li>
    </ul>
    <br>
<h3 class="subtitle">Orange Salad</h3>
<ul class="clean-list">
  <li>3-4 oranges, peeled and sliced</li>

  <li>3 tablespoons extra virgin olive oil</li>

  <li>Salt and freshly ground pepper</li>

  <li>Fresh mint, basil or fennel, chopped</li>
</ul>
<p>Let sit at room temperature for two hours to allow juices to mingle.</p>

    <br>
<h3>Essential Oil Play Dough</h3>
<ul class="clean-list">
  <li>1 1/2 cups all-purpose flour</li>

  <li>1 1/2 cups water</li>

  <li>3/4 cup salt</li>

  <li>3 tablespoons cream of tartar</li>

  <li>1 1/2 tablespoons vegetable oil</li>

  <li>Food Coloring: 10 drops or more in each divided dough</li>

  <li>Essential Oils: Lavender, sweet orange</li>
</ul>
<p>Bake tart at 400 degrees F for 35-45 minutes</p>

