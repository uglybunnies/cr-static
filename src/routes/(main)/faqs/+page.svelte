    <h2>Frequently Asked Qestions</h2>
    <h3>1. Who is this site for?</h3>

 

<p>It is for anyone of any age who would like new tools to support happiness, well-being and resilience during stressful and emotionally challenging times.</p>

 

<h3>2. Are there fees involved in this site?</h3>

 

<p>Everything on this website is offered free of cost for everyone. No fees, subscription or any kind of hidden charges are involved in this site, including the classes offered.</p>

 

<h3>3. How do I best use this web site?</h3>

 

<p>This site is a bit like a festival of new experiences. You may want to play with the rainbow arrow on the home page. Or just wander in, explore and enjoy. Then later, after seeing the menu, you can try one thing at a time.</p>

 

<p>Or you could be more linear, start at the beginning and go through one element at a time. If you feel an affinity for one of the elements, you may want to start your journey there. </p>

 

<p>Or think of your needs and see if there are topics that interest you and could be useful. For example ---</p>

 

<ul>
  <li><strong>If your spirits are low and you need uplifting</strong>, you could try going to the music chamber and singing along with songs there. For many people, singing seems to uplift the mood. You could also go to the mood-changing stories and read a few of them sent in by users of this site.</li>
  
  
  
  <li><strong>If you are in a relationship that is troublesome at the moment</strong>, you might check "the art of listening," grounding meditation or "Generosity and the Brain"</li>
  
  
  
  <li><strong>If you are annoyed or angry and interested in changing your energy</strong>, you could read "Dissolving Anger."</li>
  
  
  
  <li><strong>If you are stuck or frozen and cannot act</strong>, it may be useful to try the exercise "Inaction to Action" or "Creating New Pathways."</li>
  
  
  
  <li><strong>If you are too critical of yourself</strong>, this site offers many tools that can make a difference, such as "Kindness to Self," Quieting the Inner Critic, "Self Hypnosis," and Present Moment Meditation.</li>
</ul>

 

<h3>4. Why is this site focused on the five elements?</h3>

 

<p>For centuries, different cultures have recognized the importance of the five elements and have used them as a key to unlocking self-mastery, healing and balancing disruptive circumstances. During these unusual and difficult recent times, many connections that we had previously counted on were lost. The five elements are a constant that we can hold to and that will stay with us. They will nourish us and help bring balance back into our lives.</p>

 

